PI = 3.14

l = float(input("Enter the length: "))
b = float(input("Enter the breadth: "))

print("Area of rectangle: ", l*b, "sq. units.")

rad = float(input("Enter radius of circle: "))

print("Area of circle: ", PI*(rad**2), "sq. units.")